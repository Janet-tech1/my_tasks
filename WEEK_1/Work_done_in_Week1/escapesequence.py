#Escape sequences in print - "\n", "\t"
print("Line1\nLine2")                # New line
print("Item1\tItem2")                # Tab space
print("He said, \"Hello Jane\"")     # Quoted string
print('It\'s Python!')               # Apostrophe in single quote

# Newline (\n)
print("Welcome to Python\nLet's learn new things together!")

# Tab (\t)
print("Name\tAge\tLocation")
print("Jane\t25\tLagos")

# Quotes inside string
print("She said, \"Hello my friend!\"")
print('It\'s a sunny day.')

# Backslash
print("File path: C:\\Users\\Jane\\Documents")


